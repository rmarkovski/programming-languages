public class Test {
    private String s;

    // Constructor without parameters
    public Test() {
        // Set the value of s to "Programming languages"
        this.s = "Programming languages";
    }

    // Method to print the value of s
    public void method1() {
        System.out.println("Value of s: " + s);
    }

    // Method to print the value of n
    public void method2(int n) {
        System.out.println("Value of n: " + n);
    }

    // Private method to print a message
    private void method3() {
        System.out.println("Private method is invoked");
    }
}
